        </div><br>
        Die Figur fühlt sich zu einem der Steinhaufen am Hang hingezogen (siehe Bereich Y2). Wenn sich der Charakter innerhalb von 30 Fuß nähert, ist die Anwesenheit von Kavans magischem Speer (siehe "Schatz" unten) unter den Felsen zu spüren.<br><br>
        <p class="bold">Schatz</p><br>
        Die Felsen des Steinhaufens sind schwer, können aber zur Seite gerollt werden, so dass ein Blutspeer (siehe Anhang C) inmitten von Kavans schimmeligen Knochen liegt. Jede Kreatur kann den Speer schwingen, aber nur der von Kavan gewählte Charakter erhält einen Bonus von +2, um Angriffe auszuführen und Würfe zu beschädigen, die mit dieser magischen Waffe ausgeführt wurden.<br><br>
