        Strahd beobachtet das Verfahren, verteidigt die Druiden, wenn sie angegriffen werden, und zieht sich zurück, wenn sie übertroffen werden.<br><br>
        <p class="bold">Entwicklung</p><br>
        Wenn die Charaktere anwesend sind, wenn die Druiden das Ritual beenden, lesen Sie:<br><br>
