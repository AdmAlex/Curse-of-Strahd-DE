        </div><br>
        Die Kreatur, die aus der Holzstatue ausbricht, ist ein Baumbrand (siehe Anhang D), den die Druiden Wintersplinter nennen. Das grüne Licht kommt von dem magischen Juwel, das in sein "Herz" eingebettet ist. Der Edelstein kann nur entfernt werden, wenn Wintersplinter tot ist.<br><br>
        Die Druiden befehlen Wintersplinter, nach Norden zu reisen und dem Weinberg Wizard of Wines den Garaus zu machen (Kapitel 12). Obwohl die Charaktere vielleicht nicht verstehen, was die Druiden vorhaben, werden sie sich zweifellos fragen, wohin die Druiden die Baumfäule schicken. Wenn Wintersplinter nach Norden reist, sollte sein Ziel für diejenigen, die zuvor das Weingut und den Weinberg besucht haben, klar sein. Ob sie versuchen, seinen Vormarsch zu stoppen, liegt an ihnen.<br><br>
        Sobald die Baumfäule verschwunden ist, befiehlt Strahd den Druiden und Berserkern, den Hügel zu verlassen, damit er allein sein kann. Als sie in den Wald fliehen, blickt er sehnsüchtig auf das Bild seiner angestammten Heimat im Westen (siehe Bereich Y5).<br><br>
        <h3>Blutspeer von Kavan</h3><hr>
        Der Geist von Kavan, einem längst toten barbarischen Häuptling, reicht bis zu einem der Charaktere, vorzugsweise einem Barbaren, einem Druiden oder einem Waldläufer. Lies den folgenden Text dem Spieler dieses Charakters vor:<br><br>
        <div class="flavor">
