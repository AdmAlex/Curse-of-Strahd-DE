        <br />
        <br />
        <ul>
          <li>Nehmen Sie sich Zeit, um die verrottenden Hölzer von Gebäuden, die verblassten und mottenzerfressenen Kleider der barovianischen Bauernschaft, die wurmbehafteten Seiten alter Bücher und den Rost an eisernen Zäunen und Toren zu beschreiben.</li>
          <li>Ein Charakter, der in einen Spiegel, einen Pool oder eine andere reflektierende Oberfläche blickt, könnte eine ältere, alternde Version von sich selbst sehen.</li>
        </ul>
      </div>
