      <h2>Zeichen des Schreckens</h2>
      <hr />
      <div>
        Ein Windstoß wie der übelriechende Atem eines schrecklichen Monsters begrüßt die Abenteurer, während sie die Stufen eines Turms in Castle Ravenloft erklimmen. Als sie sich der Spitze nähern, beginnen sie das Schlagen eines Herzens in der Dunkelheit darüber zu hören. Kein menschliches Herz, sondern das Herz von etwas Monströsem und Schrecklichem. Das ist die Natur des gotischen Horrors: Angst, die durch Vorfreude und die dunkle Erkenntnis gezüchtet wird, dass alles wahrhaftig und schrecklich in der Zeit offenbart wird.
        <br />
        <br />Die folgenden Tipps können dir helfen, dieses Abenteuer zu einem kühlenden Erlebnis für dich und deine Spieler zu machen.
        <br />
        <br />
      </div>
      <h3>Das Unbekannte</h3>
      <hr />
      <div>
        Schrecken entsteht aus der Angst vor dem Unbekannten. Unsere Angst wird verstärkt, wenn die Dunkelheit uns umgibt und wir nicht sehen können, oder wenn die Wahrheit hinter einer verschlossenen Tür liegt, von einem Laken bedeckt oder in der weichen Erde begraben. Es ist nicht das Monster, sondern sein Schatten, der Schrecken hervorruft. Je mehr wir über ein Monster wissen, desto weniger fürchten wir es, also ist der Trick, es so lange wie möglich aus dem Licht zu halten. Hier sind zwei Tricks, um die Angst vor dem Unbekannten zu schüren:
        <br />
        <br />
        <ul>
          <li>Wenn es so aussieht, als hätten die Charaktere alles unter Kontrolle, kann man eine Windböe haben, die plötzlich ihre Fackeln ausbläst und sie in die Dunkelheit stürzt.</li>
          <li>Bevor ein Monster erscheint, nimm dir einen Moment Zeit, um den Geruch zu beschreiben, der ihm vorausgeht, den unheimlichen Klang, den es macht, oder den seltsamen Schatten, den es wirft.</li>
        </ul>
      </div>
      <br />
      <h3>Vorahnungen</h3>
      <hr />
      <div>
        Bei der Vorahnung geht es darum, Hinweise auf eine schreckliche Wahrheit zu finden, die noch nicht enthüllt wurde. Betrachten Sie die folgenden Beispiele:
        <br />
        <br />
        <ul>
          <li>Bevor Charaktere auf ein Monster treffen, weisen Sie mit Hinweisen wie Kratzspuren, abgenagten Knochen und Blutflecken auf die Anwesenheit des Monsters hin.</li>
          <li>Wann immer Charaktere eine lange Pause einlegen, gib einem Charakter einen prophetischen Traum, in dem er oder sie etwas sieht, das noch nicht gefunden oder angetroffen wurde.</li>
        </ul>
      </div>
