      <h3>Humor</h3>
      <hr />
      <div>
        Es gibt keine fremderen Bettgenossen als Horror und Humor. Die Spannung kann nicht auf unbestimmte Zeit aufrechterhalten werden, so dass ein Hauch von Humor eine Verschnaufpause bietet, die dem Entsetzen die Chance gibt, sich später an uns heranzuschleichen und uns überraschen zu lassen. Während humorvolle Situationen im Laufe des Abenteuers natürlich auftreten, gibt es hier einige Tipps, um bei Bedarf Humor zu kreieren:
        <br />
        <br />
        <ul>
          <li>Erlaube NPCs (auch bösen), Witze zu erzählen, mit einer lustigen Stimme zu sprechen oder sich idiotisch zu verhalten. Selbst morbider Humor ist besser als keiner.</li>
          <li>Wenn ein Held, Bösewicht oder Monster eine natürliche 1 auf einen Angriffswurf, einen Fähigkeitscheck oder einen Rettungswurf wirft, beschreiben Sie ein humorvolles Missgeschick, das als Folge des niedrigen Wurfes auftritt, wie z.B. ein Charakter, der versehentlich eine Lampe umwirft und einige Vorhänge in Brand setzt, während er versucht, sich zu verstecken oder sich lautlos zu bewegen.</li>
        </ul>
