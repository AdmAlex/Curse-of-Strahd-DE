      <h3>Licht</h3>
      <hr />
      <div>
        Eine Geschichte, die immer wieder dunkel im Ton ist, wird sehr schnell lästig. Es muss den gelegentlichen Lichtstrahl als Kontrast und zur Schaffung eines Gefühls der Hoffnung beinhalten. Monster und andere Schrecken müssen mit freundlichen und liebenswerten Kreaturen ausgeglichen werden, was den Charakteren noch mehr Gründe gibt, sich gegen die Dunkelheit zu wehren. Hier sind ein paar Möglichkeiten, um einem tragischen Märchen einen Lichtschimmer hinzuzufügen:
        <br />
        <br />
        <ul>
          <li>In einem so tristen Land wie Barovia nehmen Sie sich die Zeit, die gelegentliche Szene der Schönheit zu beschreiben, wie zum Beispiel eine hübsche Blume, die auf einem Grab wächst.</li>
          <li>Stelle sicher, dass die Helden Kontakt zu ehrlichen, freundlichen und hilfsbereiten NSCs haben, wie den Martikovs in Vallaki oder den Krezkovs in Krezk.</li>
        </ul>
      </div>
      <br />
      <h3>Personifizierung</h3>
      <hr />
      <div>
        Die Zuordnung menschlicher Eigenschaften zu einem unbelebten Ding ist eine Möglichkeit, etwas Gewöhnliches in etwas Bösartiges zu verwandeln. Ein stöhnendes Haus, der heulende Wind, der greifende Schlamm und eine hockende Truhe sind nicht nur alltägliche Dinge - sie sind Charaktere in deiner Geschichte, die dank ihrer menschlichen Eigenschaften umso gruseliger geworden sind. Fackeln sprühen nervös, rostige Scharniere erschüttern die Stille mit ihren plötzlichen Schreien der Qualen, und Spinnweben rufen uns leise zu unserem Untergang. Hier sind weitere Beispiele:
        <br />
        <br />
        <ul>
          <li>Stellen Sie sich die Dunkelheit als eine stille Menge vor, die den Charakteren überall hin folgt und sie im Schlaf anstarrt.</li>
          <li>Stellen Sie sich Bäume als gewaltige Riesen vor, die untätig und doch immer wachsam stehen, wenn die Charaktere allein den Gefahren des Svalicher Waldes ausgesetzt sind.</li>
        </ul>
      </div>
      <br />
      <h3>Details</h3>
      <hr />
      <div>
        In einer Horrorgeschichte ist nicht zu erkennen, wo die Gefahr lauert. Ein blickender Wasserspeier kann ein getarntes Monster oder nur eine teuflische Skulptur sein. Ein an einer Wand hängender Spiegel könnte die Macht haben, alle, die in ihn blicken, zu durchdringen, oder er könnte nichts Ungewöhnliches sein. In einer Horrorgeschichte lenkt die Zeit, die man sich nimmt, um ein Objekt im Detail zu beschreiben, die Aufmerksamkeit auf es, macht es misstrauisch und könnte von der realen Gefahr ablenken. Hier sind ein paar Tricks, die du benutzen kannst:
        <br />
        <br />
        <ul>
          <li>Wählen Sie in einem bestimmten Begegnungsbereich ein Objekt oder eine Funktion aus, die Sie detailliert beschreiben möchten. Es muss nicht wichtig für die Geschichte sein.</li>
          <li>Erlaube dem Charakter, der die höchste passive Weisheit (Wahrnehmung) hat, etwas zu sehen, zu hören oder zu riechen, was niemand sonst wahrnehmen kann.</li>
        </ul>
      </div>
