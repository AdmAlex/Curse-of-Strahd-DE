        <p class="bunter">Karte 5.2: Blue Water Inn (Bereich N2)</p>
        <br>
        <p class="bold">Hüter der Feder</p>
        Urwin Martikov (LG-Mensch) ist ein Werrabe (siehe Anhang D) und ein hochrangiges Mitglied der Hüter der Feder, eines Geheimbundes von Wereraben, der sich gegen Strahd stellt. Urwins Ehefrau und Geschäftspartnerin, Danika Dorakova (LG weiblicher Mensch), ist ebenfalls ein Werrabe, ebenso wie ihre beiden Söhne Brom und Bray. Die Jungen haben jeweils nur 7 Trefferpunkte und sind mit elf und neun Jahren zu jung, um effektive Kämpfer zu sein.<br><br>
        Zu jeder Zeit sind weitere 1d4 Werraben (Mitglieder der Hüter der Feder) im Gasthaus anwesend, entweder auf dem Dach in Rabenform oder in menschlicher Gestalt zusammengekauert. Diese Werraben sind loyale Freunde der Martikovs und helfen beim Schutz des Gasthauses.<br><br>
        Wenn die Figuren das Vertrauen der Werraben in Vallaki verdienen, werden die Hüter der Feder auf sie aufpassen. Wenn die Charaktere das nächste Mal in ernsthafte Schwierigkeiten geraten, können Sie eine Gruppe von 1d4 Werraben auftauchen lassen, um sie zu retten oder ihnen anderweitig zu helfen.<br><br>
        <p class="bold">Die Schicksale von Ravenloft</p>
        Wenn Ihre Kartenlesung zeigt, dass ein Schatz im Gasthaus versteckt ist, verraten die Wächter der Feder erst dann, wo der Schatz liegt, wenn sie wissen, dass die Figuren in der Lage sind, ihn zu beschützen. Um ihre Fähigkeiten zu testen, gibt Urwin den Charakteren die folgende Quest:<br><br>
        <div class="flavor">
        Urwin nimmt Sie zur Seite und hält seine Stimme tief. "Mein Weinvorrat ist fast aufgebraucht, und die nächste Lieferung ist überfällig. Ich gebe Euch, was Ihr sucht, wenn Ihr mir meinen Wein bringt. Der Weinberg und das Weingut befinden sich ein paar Meilen westlich von hier. Folgen Sie einfach der alten Svalichstraße und den Schildern."<br>
        </div><br>
        Urwin versäumt es zu erwähnen, dass sein mürrischer Vater, Davian Martikov, Eigentümer der örtlichen Weinkellerei und des Weinbergs ist, der Zauberer der Weine (Kapitel 12). Es gibt böses Blut zwischen Urwin und seinem Vater (den Urwin und Danika als "die alte Krähe" bezeichnen). Obwohl Urwin das Weingut leicht selbst besuchen könnte, hält er den Umgang mit seinem Vater für eine würdige Prüfung der Kompetenz der Figuren, und er hält sein Versprechen ein, wenn sie die Suche abschließen und mit seiner Weinlieferung zurückkehren.<br><br>
        Urwin schickt einen Wereraven in Rabenform, um den Fortschritt der Partei aus der Ferne zu beobachten. Wenn die Charaktere in Schwierigkeiten geraten, meldet sich der Wererabe sofort bei Urwin.<br><br>
        <p class="bold">N2a. Brunnen</p><br>
        Ein drei Meter hoher Steinrand umgibt die Mündung dieses 40 Fuß tiefen, moosbewachsenen Brunnens. Aus diesem Brunnen schöpft das Gasthaus frisches Wasser.<br><br>
        <p class="bold">N2b. Außentreppe</p><br>
        Eine Holztreppe führt über die Außenwand des Gasthauses hinauf zu den Gästezimmern im Obergeschoss (Bereiche N2l und N2m). Die robuste Holztür am oberen Ende der Treppe kann von innen vergittert werden.<br><br>
