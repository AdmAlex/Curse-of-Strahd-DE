        <h3>Stadttore</h3>
        <hr />
        Drei hohe Tore aus Eisenstangen führen in die Stadt:<br><br>
        <ul>
          <li>Das Nordtor wird manchmal auch als "Zarovich-Tor" oder "Tor zum See" bezeichnet, weil es zum Zarovich-See führt (Kapitel 2, Bereich L).</li>
          <li>Das Westtor wird als Sonnenuntergangstor bezeichnet, obwohl kein Mensch in Vallaki einen ungetrübten Sonnenuntergang gesehen hat. Einige verlassene Hütten säumen die Straße vor diesem Tor.</li>
          <li>Das Osttor ist auch als das Morgen-Tor oder, wie es einige Einheimische gerne nennen, als Trauer-Tor bekannt.</li>
        </ul><br>
        Schwere Eisenketten mit eisernen Vorhängeschlössern halten die Tore nachts geschlossen. Tagsüber sind die Tore zwar geschlossen, aber normalerweise nicht verschlossen.<br><br>
        Zwei Stadtwachen (LG-Männer und Frauen) stehen direkt vor jedem Tor. Anstelle von Speeren tragen sie Lanzen (Reichweite von 10 ft., 1d10 + 1 piercing damage bei einem Treffer). Diese Waffen sind lang genug, um Kreaturen durch die Gitterstäbe des Tores zu stechen. Die Wachen begrüßen alle Besucher mit Misstrauen, insbesondere diejenigen, die nachts ankommen. Wenn die Charaktere nachts ankommen, müssen einer oder mehrere von ihnen bei einer DC 20 Charisma (Persuasion) erfolgreich sein, um die Wachen zu überzeugen, das Tor aufzuschließen und sie eintreten zu lassen.<br><br>
        Wenn an einem der Tore Ärger ausbricht, schreien die Wachen dort "Zu den Waffen!". Ihre Rufe werden in ganz Vallaki widerhallen und die gesamte Stadt innerhalb von Minuten in Alarmbereitschaft versetzen. Vallaki hat vierundzwanzig menschliche Wachen, von denen die Hälfte jederzeit im Dienst ist (sechs stehen an den Toren Wache, sechs patrouillieren an den Mauern). Die Stadt kann auch eine Miliz von fünfzig körperlich gesunden, mit Knüppeln, Dolchen und Fackeln bewaffneten menschlichen Bürgern aufstellen.<br><br>
