        <h3 id="2_1">N1. Kirche St. Andral</h3>
        <hr />
        Es wird keine Karte der Kirche zur Verfügung gestellt. Falls es notwendig wird, nehmen wir an, dass diese Kirche die gleiche Konfiguration wie die im Dorf Barovia (Kapitel 3, Bereich E5) hat, aber ohne den Unterboden.<br><br>
        <div class="flavor">
        Diese schiefe, jahrhundertealte Steinkirche hat einen gewölbten Kirchturm im hinteren Teil und Wände, die mit gesprungenen, farbigen Glasfenstern mit der Darstellung frommer Heiliger gesäumt sind. Ein Zaun aus Schmiedeeisen umschließt einen Garten mit Grabsteinen neben der Kirche. Ein dünner Nebel kriecht zwischen den Gräbern hindurch.
        </div><br>
        Diese Kirche ist dem Morninglord gewidmet und nach dem heiligen Andral benannt, dessen Gebeine einst unter dem Altar ruhten (siehe den Abschnitt "Gebeine des heiligen Andral").<br><br>
        Pater Lucian Petrovich (LG-Menschpriester) beaufsichtigt die Kirche und tut sein Bestes, um die Stimmung zu heben. Ihm zur Seite steht ein Waisen- und Altarjunge namens Yeska (LG-Mensch, männlich, nicht kämpfend). Ein kräftiger Bursche mit einer ständig gefurchten Stirn namens Milivoj (siehe unten) kümmert sich um den Boden und gräbt Gräber aus.<br><br>
        Nachts ist die Kirche voll von 2d6 + 6 verängstigten vallakischen Erwachsenen (LG männliche und weibliche menschliche Bürgerliche) und 2d6 gleichermaßen verängstigten vallakischen Kindern (LG männliche und weibliche menschliche Nichtkämpfer). Pater Lucian bietet seiner nächtlichen Gemeinde seine Gebete und das Versprechen des Schutzes des Heiligen Andral an. Unter der nächtlichen Herde von Pater Lucian befindet sich eine traurige alte Frau namens Willemina Rikalova. Ihr Sohn, der Schuhmacher Udo Lukowitsch, wurde inhaftiert, weil er sich gegen den Bürgermeister ausgesprochen hat (siehe Bereich N3m). Sie betet, dass ihr Sohn freigelassen wird.<br><br>
