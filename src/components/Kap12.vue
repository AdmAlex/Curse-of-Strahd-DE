        Die Martikovs entkommen dem Blutbad nur knapp und fliehen zum Blue Water Inn in Vallaki (Kapitel 5, Bereich N2). Davian Martikov wird durch den Verlust des Weinguts zerstört, und die Moral in Vallaki sinkt auf einen historischen Tiefpunkt, als sich die Nachricht von der Zerstörung des Weinguts durch die Stadt verbreitet.<br><br>
